#include <iostream>

void foo() {
    static int a = 0;  // Явная инициализация статической переменной
    int b = 0;         // Инициализация локальной переменной
    
    a++; 
    b++; 
    
    std::cout << a << " " << b << '\n';
}

int main() {
    std::cout << "Демонстрация статических и локальных переменных:\n";
    std::cout << "Формат: [статическая_a] [локальная_b]\n\n";
    
    for (int i = 0; i < 10; i++) {
        std::cout << "Итерация " << (i + 1) << ": ";
        foo();
    }
    
    return 0;
}

